package com.watson.pureenjoy.music.http.entity.recommend;

public class RecommendModuleInfo {
    private String id;
    private int style;
    private String link_url;
    private String style_nums;
    private int pos;
    private String title;
    private String key;
    private String picurl;
    private String title_more;
    private int nums;
    private String jump;

    public String getId() {
        return id;
    }

    public int getStyle() {
        return style;
    }

    public String getLink_url() {
        return link_url;
    }

    public String getStyle_nums() {
        return style_nums;
    }

    public int getPos() {
        return pos;
    }

    public String getTitle() {
        return title;
    }

    public String getKey() {
        return key;
    }

    public String getPicurl() {
        return picurl;
    }

    public String getTitle_more() {
        return title_more;
    }

    public int getNums() {
        return nums;
    }

    public String getJump() {
        return jump;
    }
}
